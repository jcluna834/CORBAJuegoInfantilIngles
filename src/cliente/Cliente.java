    private void jRadioButtonAdminModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jRadioButtonAdminModificarActionPerformed
        int codDic = Integer.parseInt(JOptionPane.showInputDialog(null, "Ingresa el codigo del diccionario a modificar"));
        int codPal = Integer.parseInt(JOptionPane.showInputDialog(null, "Ingresa el codigo de la palabra a modificar"));
        String anteriorPalabra = servidor.getPalabra(codPal);
        String[] aux = anteriorPalabra.split(";");
        String nuevaPalabra = codPal + ";";
        boolean cambio = false;
        String ingles = JOptionPane.showInputDialog(null, "Ingresa la nueva palabra para " + aux[1]);
        if (ingles != null && !ingles.isEmpty()) {
            nuevaPalabra += ingles + ";";
            cambio = true;
        } else {
            nuevaPalabra += aux[1] + ";";
        }
        String espanol = JOptionPane.showInputDialog(null, "Ingresa la nueva palabra para " + aux[2]);
        if (espanol != null && !espanol.isEmpty()) {
            nuevaPalabra += espanol;
            cambio = true;
        } else {
            nuevaPalabra += aux[2];
        }
        if (cambio && servidor.modificarPalabra(anteriorPalabra, nuevaPalabra, codDic)) {
            JOptionPane.showMessageDialog(null, "Palabra modificada correctamente");
        } else {
            JOptionPane.showMessageDialog(null, "No se pudo modificar la palabra");
        }
    }//GEN-LAST:event_jRadioButtonAdminModificarActionPerformed

